package _1277_count_square_submatrices_with_all_ones

import (
	"testing"
)

func Test_countSquares(t *testing.T) {
	tests := []struct {
		name   string
		matrix [][]int
		want   int
	}{
		{
			name: "1",
			matrix: [][]int{
				{0, 1, 1, 1},
				{1, 1, 1, 1},
				{0, 1, 1, 1},
			},
			want: 15,
		},
		{
			name: "2",
			matrix: [][]int{
				{1, 0, 1},
				{1, 1, 0},
				{1, 1, 0},
			},
			want: 7,
		},
		{
			name: "3",
			matrix: [][]int{
				{1},
			},
			want: 1,
		}, {
			name: "4",
			matrix: [][]int{
				{0},
			},
			want: 0,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := countSquares(tt.matrix); got != tt.want {
				t.Errorf("countSquares() = %v, want %v", got, tt.want)
			}
		})
	}
}

func BenchmarkCountSquares(b *testing.B) {
	var res int
	for i := 0; i < b.N; i++ {
		res = countSquares(bigMatrix)
	}
	_ = res
}

